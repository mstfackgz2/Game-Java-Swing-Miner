package models;



public class Location {
    private int locX=0;
    private int locY=0;

    public Location(int locX, int locY) {
        this.locX = locX;
        this.locY = locY;
    }
    public Location() {

    }

    public int getLocX() {
        return locX;
    }

    public void setLocX(int locX) {
        this.locX = locX;
    }

    public int getLocY() {
        return locY;
    }

    public void setLocY(int locY) {
        this.locY = locY;
    }
}
